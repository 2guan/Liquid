/** Fisher–Yates sample of `count` distinct seeds (default 7–8 shown at a time). */
export function sampleSeeds(count = 8): MoodSeed[] {
  const pool = MOOD_SEEDS.slice();
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [pool[i], pool[j]] = [pool[j], pool[i]];
  }
  return pool.slice(0, Math.min(count, pool.length));
}

/** A random mood line, avoiding `exclude` (the text already in the box). */
export function randomPrompt(exclude?: string): string {
  let p = MOOD_PROMPTS[Math.floor(Math.random() * MOOD_PROMPTS.length)];
  let guard = 0;
  while (exclude && p === exclude && guard++ < 8) {
    p = MOOD_PROMPTS[Math.floor(Math.random() * MOOD_PROMPTS.length)];
  }
  return p;
}
