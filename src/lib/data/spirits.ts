function build(): Spirit[] {
  const out: Spirit[] = [];
  (Object.keys(RAW) as SpiritCategory[]).forEach((cat) => {
    RAW[cat].forEach((row, i) => {
      const [name, nameEn, family, abv, origin, note, flavors] = row;
      out.push({
        id: `${cat}-${i}`,
        name,
        nameEn,
        category: cat,
        family,
        abv,
        origin,
        note,
        flavor: flavors.split("|"),
      });
    });
  });
  return out;
}

export const SPIRITS: Spirit[] = build();

export const SPIRIT_COUNT = SPIRITS.length;

export const spiritById = (id: string): Spirit | undefined =>
  SPIRITS.find((s) => s.id === id);

export const spiritsByCategory = (cat: SpiritCategory): Spirit[] =>
  SPIRITS.filter((s) => s.category === cat);

export function searchSpirits(query: string): Spirit[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return SPIRITS.filter(
    (s) =>
      s.name.includes(q) ||
      s.nameEn.toLowerCase().includes(q) ||
      s.origin.includes(q) ||
      s.flavor.some((t) => t.includes(q)),
  );
}
